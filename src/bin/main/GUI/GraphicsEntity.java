package bin.main.GUI;

import java.awt.*;

public class GraphicsEntity {
    private String IMAGE_FILE = null;
    private int WIDTH;
    private int HEIGHT;
    private int X;
    private int Y;

    public void image_file(String s)    { this.IMAGE_FILE = s; }
    public void width(int width)        { this.WIDTH = width; }
    public void height(int height)      { this.HEIGHT = height; }
    public void x(int x)                { this.X = x; }
    public void y(int y)                { this.Y = y; }




}
